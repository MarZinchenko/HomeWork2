package HomeWork2.loops;

public class LoopsMain5 {
    public static void main(String[] args) {

        for (int i = 2; i <= 10; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.print(i + " x " + j + " = " + (i * j) + "\n");
            }
                System.out.println();
            }
        }
    }